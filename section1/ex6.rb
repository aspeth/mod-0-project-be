# assigns 10 to variable types_of_people
types_of_people = 10

# assigns string with interpolation to variable x
x = "There are #{types_of_people} types of people."

# assigns string binary to variable binary
binary = "binary"

# assigns don't to variable do_not
do_not = "don't"

# assigns string with interpolation to variable y
y = "Those who know #{binary} and those who #{do_not}."

# prints x and y on separate lines
puts x
puts y

# prints string with interpolation repeating joke
puts "I said: #{x}."
puts "I also said: '#{y}'."

# assigns boolean false to variable hilarious
hilarious = false

# assigns string with interpolation to variable joke_evaluation
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints variable joke_evaluation
puts joke_evaluation

# assigns strings to variables w and e
w = "This is the left side of..."
